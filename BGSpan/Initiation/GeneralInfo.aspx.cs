		private void ViewMenu()
		{
			Menu.Controls.Clear();
			try 
			{
				conn.QueryString = "SELECT * FROM SCREENMENU WHERE MENUCODE = '" + Request.QueryString["mc"] + "'";
				conn.ExecuteQuery();
				
				for (int i = 0; i < conn.GetRowCount(); i++) 
				{
					HyperLink t = new HyperLink();
					t.Text = conn.GetFieldValue(i, 2);
					t.Font.Bold = true;
					string strtemp = "";
					if (conn.GetFieldValue(i, 3).Trim()!= "") 
					{
						if (conn.GetFieldValue(i,3).IndexOf("mc=") >= 0)
							strtemp = "&curef=" + Request.QueryString["curef"] + "&cif=" + Request.QueryString["cif"] + "&exist=" + Request.QueryString["exist"];
						else	
							strtemp = "&mc=" + Request.QueryString["mc"]+ "&curef=" + Request.QueryString["curef"] + "&cif=" + Request.QueryString["cif"] + "&exist=" + Request.QueryString["exist"];
					}
					else 
					{
						strtemp = ""; 
					}
					t.NavigateUrl = conn.GetFieldValue(i, 3)+strtemp;
					Menu.Controls.Add(t);
					Menu.Controls.Add(new LiteralControl("&nbsp;&nbsp;&nbsp;"));
				}
			} 
			catch (Exception ex) 
			{
				string temp = ex.ToString();
			}
		}

		private void GenerateAPPNUMBER()
		{
			conn.QueryString = "EXEC GENERATE_ID '" + Session["BranchID"].ToString() + "', '0'";
			conn.ExecuteQuery();

			if (conn.GetRowCount() > 0)
			{
				TXT_APPNUMBER.Text = conn.GetFieldValue(0,0);
			}
			else
			{
				return;
			}
		}

		private void isiDDLBUSUNIT()
		{
			DDL_BUSUNIT.Items.Clear();
			DDL_BUSUNIT.Items.Add(new ListItem("--Pilih--", ""));

			conn.QueryString = "SELECT BUSSUNITID, BUSSUNITDESC FROM RFBUSINESSUNIT WHERE ACTIVE='1'";
			conn.ExecuteQuery();
			for(int i=0; i<conn.GetRowCount(); i++) 
			{
				DDL_BUSUNIT.Items.Add(new ListItem(conn.GetFieldValue(i, "BUSSUNITDESC"), conn.GetFieldValue(i, "BUSSUNITID")));
			}
			conn.ClearData();
		}

		private void isiDDLOPUNIT()
		{
			DDL_OPUNIT.Items.Clear();
			DDL_OPUNIT.Items.Add(new ListItem("--Pilih--", ""));
			conn.QueryString = "SELECT BRANCH_CODE, BRANCH_NAME FROM RFBRANCH WHERE ACTIVE='1'";
			conn.ExecuteQuery();
			for (int i=0; i<conn.GetRowCount(); i++)
			{
				DDL_OPUNIT.Items.Add(new ListItem(conn.GetFieldValue(i,1), conn.GetFieldValue(i,0)));
			}
			
		}

		private void isiPROGRAM()
		{
			//muncul sesuai dengan DDL business unit yang terpilih, pastikan atribut "Autopostback" = true di html
			DDL_PROGRAM.Items.Clear();
			DDL_PROGRAM.Items.Add(new ListItem("--Pilih--", ""));

			conn.QueryString = "SELECT PROGRAMID, PROGRAMDESC FROM RFPROGRAM WHERE AREAID = '" + Session["AreaID"] + 
				"' AND ACTIVE = '1' AND BUSINESSUNIT = '" + DDL_BUSUNIT.SelectedValue  + "'";
			
			conn.ExecuteQuery();
			for(int i=0; i<conn.GetRowCount(); i++) 
			{
				DDL_PROGRAM.Items.Add(new ListItem(conn.GetFieldValue(i, "PROGRAMDESC"), conn.GetFieldValue(i, "PROGRAMID")));
			}
			conn.ClearData();
		}

		

