		protected void BTN_SAVE_Click(object sender, System.EventArgs e)
		{
			string ap_number= TXT_APPNUMBER.Text;

			try
			{
				conn.QueryString = "exec BGS_INITIATION_INSERT '" + 
					ap_number + "', '" + 
					Request.QueryString["curef"] + "', '" +
					TXT_REGION.Text + "', '" +
					DDL_PROGRAM.SelectedValue + "', '" +
					DDL_OPUNIT.SelectedValue + "', " + 
					tool.ConvertDate(TXT_APP_DATE_DAY.Text , DDL_APP_DATE_MONTH.SelectedValue, TXT_APP_DATE_YEAR.Text) + ", '" +
					Session["AreaName"].ToString()+ "', '" + 
					DDL_BUSUNIT.SelectedValue + "'";
				conn.ExecuteNonQuery();
			}

			catch (Exception ex)
			{
				string errmsg = ex.Message.Replace("'","");
				if (errmsg.IndexOf("Last Query:") > 0)
					errmsg = errmsg.Substring(0, errmsg.IndexOf("Last Query:"));
				GlobalTools.popMessage(this, errmsg);
				return;
			}

			Session["curef"]	= Request.QueryString ["curef"];
			Session["tc"]		= Request.QueryString ["tc"];
			Session["mc"]		= Request.QueryString ["mc"];

			Response.Redirect("DetailDataEntry.aspx?ap_number=" + ap_number + "&curef=" + Request.QueryString["curef"] + "&tc=" + Request.QueryString["tc"] + "&mc=" + Request.QueryString["mc"] + "&Prog=" + DDL_PROGRAM.SelectedValue + "&exist=" + Request.QueryString["exist"]);


		
		}

